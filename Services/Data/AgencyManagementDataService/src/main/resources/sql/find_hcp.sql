-- KEYWORD: FIRST_NAME (SPACE) LAST_NAME
SELECT * FROM (SELECT ROWNUM ROW_NUMBER, COUNT(*) OVER() TOTAL_ROWS, R1.* FROM (

  SELECT * FROM (

      (SELECT * FROM BE_HCP_LKUP
        WHERE ((UPPER(HCP_FIRST_NAME) LIKE '%A%' AND UPPER(HCP_FIRST_NAME) LIKE '%S%')
          OR (UPPER(HCP_FIRST_NAME) LIKE '%S%' AND UPPER(HCP_FIRST_NAME) LIKE '%A%'))
        AND (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1)
        AND BE_ID = '1'
        ORDER BY HCP_LAST_NAME ASC
    )
  )

) R1)

WHERE ROW_NUMBER BETWEEN 1 AND 10;

-- KEYWORD: FIRST_NAME/LAST_NAME
SELECT * FROM (SELECT ROWNUM ROW_NUMBER, COUNT(*) OVER() TOTAL_ROWS, R1.* FROM (

  SELECT * FROM (

      (SELECT * FROM BE_HCP_LKUP
        WHERE ((UPPER(HCP_FIRST_NAME) LIKE '%TEST%' OR UPPER(HCP_FIRST_NAME) LIKE '%L%')
          OR (UPPER(HCP_FIRST_NAME) LIKE '%L%' OR UPPER(HCP_FIRST_NAME) LIKE '%TEST%'))
        AND (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1)
        AND BE_ID = '1'
        ORDER BY HCP_LAST_NAME ASC
    )
  )

) R1)

WHERE ROW_NUMBER BETWEEN 1 AND 10;
