SELECT 1 FROM TIMESHEET_DET WHERE TIMESHEET_DET.TIMESHEET_SUMMARY_SK = 3541 AND TIMESHEET_DET.PR_RATE_AMT = 10.50;

SELECT 1 FROM TIMESHEET_DET WHERE TIMESHEET_DET.TIMESHEET_SUMMARY_SK = 3541 AND TIMESHEET_DET.PT_ID = '1';

SELECT 1 FROM TIMESHEET_DET T1 INNER JOIN (SELECT BE_ID,PT_ID,PT_FIRST_NAME FROM PT
  WHERE TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1) T2 ON T1.BE_ID = T2.BE_ID AND T1.PT_ID = T2.PT_ID
  WHERE UPPER(T2.PT_FIRST_NAME) LIKE '%JAC%' AND T1.TIMESHEET_SUMMARY_SK = 4236;

SELECT DISTINCT 1 FROM TIMESHEET_DET TD_T1 LEFT JOIN (SELECT VISIT_SK,AUTH_SK FROM VISIT_AUTH) TD_T3 ON TD_T1.VISIT_SK = TD_T3.VISIT_SK
  LEFT JOIN (SELECT AUTH_SK,SVC_NAME FROM AUTH_SVC) TD_T4 ON TD_T3.AUTH_SK = TD_T4.AUTH_SK
  WHERE TD_T1.TIMESHEET_SUMMARY_SK = 3541 AND TD_T4.SVC_NAME IN ('PCA');

SELECT DISTINCT 1 FROM TIMESHEET_DET TD_T1 LEFT JOIN (SELECT VISIT_SK,AUTH_SK FROM VISIT_AUTH) TD_T3 ON TD_T1.VISIT_SK = TD_T3.VISIT_SK
                        LEFT JOIN (SELECT AUTH_SK,SVC_NAME FROM AUTH_SVC) TD_T4 ON TD_T3.AUTH_SK = TD_T4.AUTH_SK
                        WHERE TD_T1.TIMESHEET_SUMMARY_SK = 3541 AND TD_T4.SVC_NAME IN ('PCA');
