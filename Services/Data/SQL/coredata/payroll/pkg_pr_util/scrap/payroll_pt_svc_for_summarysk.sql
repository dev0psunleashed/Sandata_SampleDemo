SELECT  T1.VISIT_SK,
        T1.PT_ID,
        T2.PT_FIRST_NAME,
        T2.PT_LAST_NAME,
        T4.SVC_NAME
  FROM TIMESHEET_DET T1

  LEFT JOIN (SELECT BE_ID,PT_ID,PT_FIRST_NAME,PT_LAST_NAME
    FROM PT
      WHERE (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1)) T2
  ON T1.BE_ID = T2.BE_ID AND T1.PT_ID = T2.PT_ID

  LEFT JOIN (SELECT VISIT_SK,AUTH_SK
    FROM VISIT_AUTH) T3
  ON T1.VISIT_SK = T3.VISIT_SK

  LEFT JOIN (SELECT AUTH_SK,SVC_NAME
    FROM AUTH_SVC) T4
  ON T3.AUTH_SK = T4.AUTH_SK

WHERE TIMESHEET_SUMMARY_SK = 4236;