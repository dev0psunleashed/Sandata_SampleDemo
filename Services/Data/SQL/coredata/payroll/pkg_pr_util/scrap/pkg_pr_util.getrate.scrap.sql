FROM CONTR_RATE T1
  INNER JOIN (SELECT BE_ID,PT_ID,PAYER_ID,CONTR_ID,PAYER_RANK_NAME,
                SVC_NAME,PT_PAYER_EFF_DATE,PT_PAYER_TERM_DATE,REC_TERM_TMSTP,CURR_REC_IND
      FROM PT_PAYER
      WHERE TO_DATE('2016-08-22 23:59:59', 'YYYY-MM-DD HH24:MI:SS') BETWEEN PT_PAYER_EFF_DATE AND PT_PAYER_TERM_DATE
            AND (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1)) T2
  ON T1.BE_ID = T2.BE_ID
       AND T1.PAYER_ID = T2.PAYER_ID
       AND T1.CONTR_ID = T2.CONTR_ID
       AND T1.SVC_NAME = T2.SVC_NAME
       AND TO_DATE('2016-08-22 23:59:59', 'YYYY-MM-DD HH24:MI:SS') BETWEEN CONTR_RATE_EFF_DATE AND CONTR_RATE_TERM_DATE
  WHERE T1.BE_ID = '1'
      AND RATE_QLFR_CODE = 'PAY'
      AND T1.SVC_NAME = PKG_PR_UTIL.GET_AUTH_SVC('1', 77103, TO_DATE('2016-08-22 23:59:59', 'YYYY-MM-DD HH24:MI:SS'))
