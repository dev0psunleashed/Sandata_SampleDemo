SELECT DISTINCT
          T1.STAFF_EMPLT_STATUS_TYP_NAME,
          T1.CHANGE_REASON_CODE,
          T1.CHANGE_REASON_MEMO,
          T1.REC_EFF_TMSTP
FROM STAFF T1
WHERE STAFF_ID = '20160708113523717'
  AND BE_ID = '1'
  AND CURR_REC_IND = 1
ORDER BY REC_EFF_TMSTP DESC;

SELECT T1.* FROM (
    SELECT STAFF_HIRE_DATE
      FROM STAFF
    WHERE BE_ID = '1'
      AND CURR_REC_IND = 1
      AND STAFF_ID = '20160708113523717'
      AND UPPER(STAFF_HIRE_DATE_QLFR) = 'HIRE'
      ORDER BY REC_EFF_TMSTP DESC) T1
WHERE ROWNUM <= 1;

SELECT T1.* FROM (
    SELECT STAFF_HIRE_DATE
      FROM STAFF
    WHERE BE_ID = '1'
      AND CURR_REC_IND = 1
      AND STAFF_ID = '20160708113523717'
      AND UPPER(STAFF_HIRE_DATE_QLFR) = 'REHIRE'
      ORDER BY REC_EFF_TMSTP DESC) T1
WHERE ROWNUM <= 1;
