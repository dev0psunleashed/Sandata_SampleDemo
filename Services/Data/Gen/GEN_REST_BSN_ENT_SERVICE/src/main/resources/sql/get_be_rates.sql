SELECT COUNT(*) AS TOTAL FROM BE_RATE
WHERE BE_ID = '1'
  AND SVC_NAME = 'RN'
  AND UPPER(RATE_TYP_NAME) = 'HOURLY'
  AND RATE_QLFR_CODE = 'PAY'
  AND (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1);

