SELECT * FROM (SELECT ROWNUM ROW_NUMBER, COUNT(*) OVER() TOTAL_ROWS, R1.* FROM (

  SELECT * FROM (

      (SELECT *
        FROM VISIT
          WHERE PT_ID = '877889' AND BE_ID = '1'

          AND ((VISIT_ACT_START_TMSTP BETWEEN TO_DATE('2016-03-30 00:00', 'YYYY-MM-DD HH24:MI') AND
                  TO_DATE('2016-03-30 23:59', 'YYYY-MM-DD HH24:MI')) OR
               (VISIT_ADJ_START_TMSTP BETWEEN TO_DATE('2016-03-30 00:00', 'YYYY-MM-DD HH24:MI') AND
                  TO_DATE('2016-03-30 23:59', 'YYYY-MM-DD HH24:MI'))
              )

          AND (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1)

          ORDER BY REC_CREATE_TMSTP ASC)
  )

) R1)

WHERE ROW_NUMBER BETWEEN 1 AND 10;
