SELECT * FROM (SELECT ROWNUM ROW_NUMBER, COUNT(*) OVER() TOTAL_ROWS, R1.* FROM (

  SELECT * FROM (

      (SELECT *
          FROM COUNTY_SUBDIV_LKUP
          WHERE (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1)
            AND (COUNTY_SUBDIV_FIPS_CODE IN ('90540','91170','91314'))
            AND (COUNTY_SUBDIV_QLFR IN ('CCD'))
            AND (UPPER(COUNTY_SUBDIV_NAME) LIKE '%CCD%')
            AND (COUNTY_FIPS_CODE IN ('013'))
            AND (STATE_FIPS_CODE IN ('01'))
            AND (STATE_CODE IN ('AL'))
            --NOTE: S has newline char -- AND (TRIM(COUNTY_SUBDIV_FUNC_STATUS_CODE) IN ('S'))
          ORDER BY STATE_CODE ASC
    )
  )

) R1)

WHERE ROW_NUMBER BETWEEN 1 AND 10;
