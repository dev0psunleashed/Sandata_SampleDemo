SELECT * FROM (SELECT ROWNUM ROW_NUMBER, COUNT(*) OVER() TOTAL_ROWS, R1.* FROM (

  SELECT * FROM (

      (SELECT *
          FROM MED_LKUP
          WHERE (UPPER(MED_GENERIC_NAME) LIKE '%BUTANOL%' OR UPPER(MED_DESC) LIKE '%BUTANOL%')
          ORDER BY MED_GENERIC_NAME ASC)
  )

) R1)

WHERE ROW_NUMBER BETWEEN 1 AND 10;
