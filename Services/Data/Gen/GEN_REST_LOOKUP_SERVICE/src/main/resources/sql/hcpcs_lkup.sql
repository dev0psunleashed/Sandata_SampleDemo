SELECT * FROM (SELECT ROWNUM ROW_NUMBER, COUNT(*) OVER() TOTAL_ROWS, R1.* FROM (

  SELECT * FROM (

      (SELECT *
          FROM HCPCS_LKUP
          WHERE UPPER(HCPCS_CODE) LIKE '%1F%' AND (UPPER(HCPCS_SHORT_DESC) LIKE '%HEART%' OR UPPER(HCPCS_LONG_DESC) LIKE '%HEART%')
          ORDER BY HCPCS_CODE ASC)
  )

) R1)

WHERE ROW_NUMBER BETWEEN 1 AND 10;
