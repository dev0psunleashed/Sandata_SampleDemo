SELECT * FROM (SELECT ROWNUM ROW_NUMBER, COUNT(*) OVER() TOTAL_ROWS, R1.* FROM (

  SELECT * FROM (

      (SELECT *
          FROM COUNTY_LKUP
          WHERE (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1)
            AND (COUNTY_FIPS_CODE IN ('107','109','111'))
            AND (UPPER(COUNTY_NAME) LIKE '%COUNTY%')
            AND (STATE_FIPS_CODE IN ('01','05'))
            AND (STATE_CODE IN ('AL','AR','NY'))
            AND (TRIM(FIPS_CLS_CODE) IN ('H1','H4'))
          ORDER BY COUNTY_NAME ASC
    )
  )

) R1)

WHERE ROW_NUMBER BETWEEN 1 AND 10;
