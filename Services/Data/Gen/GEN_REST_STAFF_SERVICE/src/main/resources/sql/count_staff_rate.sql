-- Check for any records that would overlap the Effective Date
SELECT COUNT(*) AS TOTAL
  FROM STAFF_RATE
WHERE BE_ID = '1' AND STAFF_ID ='1'
  AND SVC_NAME = 'RN' AND RATE_SUB_TYP_NAME = 'REGULAR' AND RATE_QLFR_CODE = 'PAY'
  AND (TO_DATE('2016-06-21 12:00:00', 'YYYY-MM-DD HH24:MI:SS') -- Effective Date/Time
    BETWEEN STAFF_RATE_EFF_DATE AND STAFF_RATE_TERM_DATE)
  AND (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1);

-- Check for any records that would overlap the Term Date
SELECT COUNT(*) AS TOTAL
  FROM STAFF_RATE
WHERE BE_ID = '1' AND STAFF_ID ='1'
  AND SVC_NAME = 'RN' AND RATE_SUB_TYP_NAME = 'REGULAR' AND RATE_QLFR_CODE = 'PAY'
  AND (TO_DATE('2016-06-21 12:00:00', 'YYYY-MM-DD HH24:MI:SS') -- Term Date/Time
    BETWEEN STAFF_RATE_EFF_DATE AND STAFF_RATE_TERM_DATE)
  AND (TO_CHAR(REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND CURR_REC_IND = 1);
