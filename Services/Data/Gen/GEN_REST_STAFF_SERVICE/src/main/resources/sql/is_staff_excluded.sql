SELECT DISTINCT T1.STAFF_ID,T1.BSN_ENT_ID,T1.SCHED_PTC_EXCL_TYPE
  FROM SCHED_PTC_EXCL T1
LEFT JOIN (SELECT STAFF_ID,BSN_ENT_ID,REC_TERM_TMSTP,CURR_REC_IND
  FROM STAFF) T2
ON T1.STAFF_ID = T2.STAFF_ID AND T1.BSN_ENT_ID = T2.BSN_ENT_ID
WHERE T1.PATIENT_ID = '20160329031744272' AND T2.STAFF_ID = '20160329031736608' AND T1.BSN_ENT_ID = '1' AND UPPER(SCHED_PTC_EXCL_TYPE) = 'EXCLUDE_STAFF' AND
       (TO_CHAR(T1.REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND T1.CURR_REC_IND = '1') AND
       (TO_CHAR(T2.REC_TERM_TMSTP, 'YYYY-MM-DD') = '9999-12-31' AND T2.CURR_REC_IND = '1');
