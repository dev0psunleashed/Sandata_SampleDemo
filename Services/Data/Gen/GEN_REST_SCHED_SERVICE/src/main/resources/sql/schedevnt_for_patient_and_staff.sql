SELECT * FROM (SELECT ROWNUM ROW_NUMBER, COUNT(*) OVER() TOTAL_ROWS, R1.* FROM (

  SELECT * FROM (

      (SELECT * FROM SCHED_EVNT
          WHERE BE_ID = '1' AND PT_ID = '114440416' AND STAFF_ID = '14444'

            AND SCHED_EVNT_START_DTIME BETWEEN
              TO_DATE('2016-06-01 00:00', 'YYYY-MM-DD HH24:MI') AND TO_DATE('2016-06-30 23:59', 'YYYY-MM-DD HH24:MI')

            ORDER BY STAFF_ID ASC
  ))

) R1)

WHERE ROW_NUMBER BETWEEN 1 AND 100;
