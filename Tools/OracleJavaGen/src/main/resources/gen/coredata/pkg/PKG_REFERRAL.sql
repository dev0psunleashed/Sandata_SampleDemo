CREATE OR REPLACE PACKAGE PKG_REFERRAL IS

TYPE REF_CURSOR IS REF CURSOR;
FUNCTION getRfrl(BE_ID VARCHAR2) RETURN REF_CURSOR;
FUNCTION getRfrl(RFRL_KEY NUMBER) RETURN REF_CURSOR;
FUNCTION getRfrl(RFRL_ARRAY STRING_ARRAY) RETURN REF_CURSOR;
FUNCTION insertRfrl(RFRL_VAR RFRL_T) RETURN NUMBER;
FUNCTION updateRfrl(RFRL_VAR RFRL_T) RETURN NUMBER;
FUNCTION deleteRfrl(RFRL_KEY NUMBER) RETURN NUMBER;

END PKG_REFERRAL;
/

CREATE OR REPLACE PACKAGE BODY PKG_REFERRAL IS

FUNCTION getRfrl(BE_ID VARCHAR2) RETURN REF_CURSOR
AS LANGUAGE JAVA NAME 'Rfrl.getRfrl(java.lang.String) return java.sql.ResultSet';
FUNCTION getRfrl(RFRL_KEY NUMBER) RETURN REF_CURSOR
AS LANGUAGE JAVA NAME 'Rfrl.getRfrl(long) return java.sql.ResultSet';
FUNCTION getRfrl(RFRL_ARRAY STRING_ARRAY) RETURN REF_CURSOR
AS LANGUAGE JAVA NAME 'Rfrl.getRfrl(oracle.sql.ARRAY) return java.sql.ResultSet';
FUNCTION insertRfrl(RFRL_VAR RFRL_T) RETURN NUMBER
AS LANGUAGE JAVA NAME 'Rfrl.insertRfrl(oracle.sql.STRUCT) return long';
FUNCTION updateRfrl(RFRL_VAR RFRL_T) RETURN NUMBER
AS LANGUAGE JAVA NAME 'Rfrl.updateRfrl(oracle.sql.STRUCT) return long';
FUNCTION deleteRfrl(RFRL_KEY NUMBER) RETURN NUMBER
AS LANGUAGE JAVA NAME 'Rfrl.deleteRfrl(long) return long';

END PKG_REFERRAL;
/

