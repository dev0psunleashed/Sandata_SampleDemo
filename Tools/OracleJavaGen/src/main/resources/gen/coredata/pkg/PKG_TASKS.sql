CREATE OR REPLACE PACKAGE PKG_TASKS IS

TYPE REF_CURSOR IS REF CURSOR;
FUNCTION getTask(BE_ID VARCHAR2) RETURN REF_CURSOR;
FUNCTION getTask(TASK_KEY NUMBER) RETURN REF_CURSOR;
FUNCTION getTask(TASK_ARRAY STRING_ARRAY) RETURN REF_CURSOR;
FUNCTION insertTask(TASK_VAR TASK_T) RETURN NUMBER;
FUNCTION updateTask(TASK_VAR TASK_T) RETURN NUMBER;
FUNCTION deleteTask(TASK_KEY NUMBER) RETURN NUMBER;

END PKG_TASKS;
/

CREATE OR REPLACE PACKAGE BODY PKG_TASKS IS

FUNCTION getTask(BE_ID VARCHAR2) RETURN REF_CURSOR
AS LANGUAGE JAVA NAME 'Task.getTask(java.lang.String) return java.sql.ResultSet';
FUNCTION getTask(TASK_KEY NUMBER) RETURN REF_CURSOR
AS LANGUAGE JAVA NAME 'Task.getTask(long) return java.sql.ResultSet';
FUNCTION getTask(TASK_ARRAY STRING_ARRAY) RETURN REF_CURSOR
AS LANGUAGE JAVA NAME 'Task.getTask(oracle.sql.ARRAY) return java.sql.ResultSet';
FUNCTION insertTask(TASK_VAR TASK_T) RETURN NUMBER
AS LANGUAGE JAVA NAME 'Task.insertTask(oracle.sql.STRUCT) return long';
FUNCTION updateTask(TASK_VAR TASK_T) RETURN NUMBER
AS LANGUAGE JAVA NAME 'Task.updateTask(oracle.sql.STRUCT) return long';
FUNCTION deleteTask(TASK_KEY NUMBER) RETURN NUMBER
AS LANGUAGE JAVA NAME 'Task.deleteTask(long) return long';

END PKG_TASKS;
/

